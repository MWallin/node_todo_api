"use strict"

// *****************************************************************************
// *****************************************************************************
// Requires

const mongoose = require( "mongoose" )




// *****************************************************************************
// *****************************************************************************
// Model definition

const User = mongoose.model( "User", {
  name: {
    type     : String,
    required : true,
    minlength: 1,
    trim     : true
  },
  email: {
    type     : String,
    required : true,
    minlength: 1,
    trim     : true

  }
})




// *****************************************************************************
// *****************************************************************************
// Requires

module.exports = {
  User
}
